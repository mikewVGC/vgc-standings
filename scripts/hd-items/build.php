function build_item_coords($data) {
    $w = 3120;
    $h = 3000;
    $x_num = 26;

    $items = json_decode($data, true);

    if (json_last_error() !== JSON_ERROR_NONE) {
        die("json error: " . json_last_error_msg() . "\n");
    }

    $x = 0;
    $y = 0;

    $final_data = [];
    foreach ($items as $item) {
        $x++;
        if ($x % 26 == 0) {
            $x = 0;
            $y++;
        }

        if (empty($item)) {
            continue;
        }

        $final_data[$item] = [ $x, $y ];
    }

    $path = "data/common/hd-item-coords.json";

    file_put_contents(__DIR__ . "/../../{$path}", json_encode($final_data));

    echo "Wrote {$path}, done\n";
}

